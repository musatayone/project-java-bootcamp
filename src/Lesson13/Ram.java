package Lesson13;

public class Ram {
    int size;
    String model;

    public Ram(int size, String kingston) {
        this.size = size;
        this.model = kingston;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }
}
